def main():
    src = open(SRC, encoding='utf-8').read()

    # テンプレート化: lp1固有文字列 → «KEY»
    template = src
    for key, exact in KEYS:
        n = template.count(exact)
        assert n == 1, f'template: {key} found {n} times'
        template = template.replace(exact, '«' + key + '»')
    # 画像は共有アセットへ
    template = template.replace('/lp1/images/', '/lp-assets/')
    # 不可視部分（コメント・aria-label）の士業語彙は汎用語に
    template = template.replace(
        '<!-- ================= 顧問先の体験（LINEモック） ================= -->',
        '<!-- ================= 相手の体験（LINEモック） ================= -->')
    template = template.replace(
        'aria-label="顧問先とのLINEトーク画面の例"',
        'aria-label="相手とのLINEトーク画面の例"')

    for lp, data in INDUSTRIES.items():
        html = template
        for key, _ in KEYS:
            assert key in data, f'{lp}: missing key {key}'
            html = html.replace('«' + key + '»', data[key])
        assert '«' not in html, f'{lp}: unresolved placeholder'
        for word in data['FORBIDDEN']:
            assert word not in html, f'{lp}: leftover "{word}"'
        outdir = os.path.join(ROOT, 'public', lp)
        os.makedirs(outdir, exist_ok=True)
        with open(os.path.join(outdir, 'index.html'), 'w', encoding='utf-8') as f:
            f.write(html)
        print(f'{lp}: written ({len(html)//1024} KB)')

    print('all LPs generated OK')


if __name__ == '__main__':
    main()
